.MODEL SMALL
.STACK 100H
.DATA
	CR EQU 0DH
	LF EQU 0AH
	MSG DB 'Enter letter: $'
	OP DB CR, LF, '$'
.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	LEA DX, MSG
	MOV AH, 9
	INT 21H
	MOV AH, 1
	INT 21H
	CMP AL, 'a'
	JL END
	CMP AL, 'z'
	JG END
	
	MOV BL, AL
	MOV AH, 2
	MOV DL, CR
	INT 21H
	MOV DL, LF
	INT 21H
	MOV DL, BL
	INT 21H

	
	END:
		MOV AH, 4CH
		INT 21H
MAIN ENDP
	END MAIN