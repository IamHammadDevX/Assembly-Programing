.MODEL SMALL
.STACK 100H
.DATA
	CR EQU 0DH
	LF EQU 0AH
	MSG DB 'Enter letter y or Y: $'
	OP DB CR, LF, '$'
.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	LEA DX, MSG
	MOV AH, 9
	INT 21H
	MOV AH, 1
	INT 21H
	CMP AL, 'y'
	JE PRINT
	CMP AL, 'Y'
	JE PRINT
	JMP DONE
	MOV DL, AL
	MOV AH, 2
	INT 21H

	
	PRINT:
		MOV DL, AL
		MOV AH, 2
		INT 21H
		JMP DONE
	DONE:
		MOV AH, 4CH
		INT 21H
MAIN ENDP
	END MAIN