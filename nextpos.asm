.MODEL SMALL
.STACK 100H
.DATA
	MSG DB 'Enter a character: $'
.CODE
MAIN PROC
	; DISPLAY MSG
    	MOV AX, @DATA    ; Initialize data segment - THIS WAS MISSING
    	MOV DS, AX
	;DISPLAY MSG
	LEA DX, MSG
	MOV AH, 9
	INT 21H
	;CHARACTER IP
	MOV AH, 1
	INT 21H
	MOV BL, AL
	;DISPLAY
	MOV AH, 2
	INT 21H
	MOV DL, ' '
	MOV DL, BL
	;DOS RETURN
	MOV AH, 4CH
	INT 21H
MAIN ENDP
	END MAIN
	