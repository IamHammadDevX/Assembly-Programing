.MODEL SMALL
.STACK 100H
.DATA
	CR EQU 0DH
	LF EQU 0AH
	MSG DB 'Enter digit 1, 3 or 2, 4 anyone: $'
    	MSGODD DB CR, LF, 'ODD$'
	MSGEVEN DB CR, LF, 'EVEN$'
.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	LEA DX, MSG
	MOV AH, 9
	INT 21H
	MOV AH, 1
	INT 21H
	SUB AL, 30H
	CMP AL, 1
	JE ODD
	CMP AL, 3
	JE ODD
	CMP AL, 2
	JE EVEN
	CMP AL, 4
	JE EVEN
	JMP DONE

	ODD:
		LEA DX, MSGODD
		MOV AH, 9
		INT 21H
		JMP DONE
	EVEN:
		LEA DX, MSGEVEN
		MOV AH, 9
		INT 21H
	DONE:
		MOV AH, 4CH
		INT 21H
MAIN ENDP
	END MAIN